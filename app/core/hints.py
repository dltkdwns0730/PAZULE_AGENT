# 대비 분석 기반 힌트 (Comparative Hints)
# (Target, Highest_Wrong) -> Hint
CONTRAST_HINTS: Dict[tuple, str] = {
    (
        "옛스럽고 빈티지한",
        "화사하고 활기찬",
    ): "지금 사진은 너무 밝고 현대적인 느낌이 강해요! 조금 더 어둡고 낡은 느낌의 장소를 찾아보세요.",
    (
        "차분하고 자연적인",
        "웅장하고 신비로운",
    ): "웅장한 느낌보다는 소박하고 평온한 자연의 모습에 더 집중해보는 건 어떨까요?",
    (
        "화사하고 활기찬",
        "옛스럽고 빈티지한",
    ): "조금 더 밝고 선명한 색감이 필요해요. 빛이 잘 드는 곳으로 이동해볼까요?",
    (
        "동화적이고 장난스러운",
        "차분하고 자연적인",
    ): "풍경보다는 아기자기한 캐릭터나 장식물에 포커스를 맞춰보세요!",
}


def get_atmosphere_hint(target: str, scores: Dict[str, float]) -> str:
    """타겟 분위기와 실제 점수를 비교하여 최적의 힌트를 반환한다."""
    # 가장 높은 점수를 얻은 잘못된 분위기 찾기
    wrong_scores = {k: v for k, v in scores.items() if k != target}
    highest_wrong = max(wrong_scores, key=wrong_scores.get)

    # 1. 대비 힌트가 있는지 확인
    hint = CONTRAST_HINTS.get((target, highest_wrong))
    if hint:
        return hint

    # 2. 없으면 해당 분위기의 기본 가이드 중 하나 반환
    import random

    guides = ATMOSPHERE_GUIDES.get(
        target, ["조금 더 해당 분위기가 잘 드러나도록 찍어보세요!"]
    )
    return random.choice(guides)
